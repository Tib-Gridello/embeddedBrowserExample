package com.jSoft.burp;

import burp.api.montoya.BurpExtension;
import burp.api.montoya.MontoyaApi;

///////////////////////////////////////////////////////////////////////////////
// CLASS Extension
///////////////////////////////////////////////////////////////////////////////
public class Extension implements BurpExtension{
@Override
public void initialize(final MontoyaApi api){
  api.extension().setName("Embedded Browser Example");

  api.userInterface().registerSuiteTab("Embedded Browser Example", new BrowserPanel(api));
}
}
///////////////////////////////////////////////////////////////////////////////
// END CLASS Extension
///////////////////////////////////////////////////////////////////////////////